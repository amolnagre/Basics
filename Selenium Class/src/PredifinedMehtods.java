
public class PredifinedMehtods {

	public static void main(String[] args) {
		// Learning Predefined Methods
		//Math.random()--> This method will return random number between 0.0 to 1.0
		
		double number=Math.random()*10;// by default Math.random are taking double value
		System.out.println(number);
		
		double numb=Math.random()*100;
		System.out.println(numb);
		
		int num=(int)(Math.random()*100);// type casting double to integer
		System.out.println(num);

	}

}
